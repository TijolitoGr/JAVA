public class ED019_Metodo04 {
    static void meuMetodo(String nome, int idade) {
        System.out.println(nome + " tem " + idade + " anos.");
    }
    
    public static void main(String[] args) {
        meuMetodo("Maria", 27);
        meuMetodo("José", 18);
        meuMetodo("António", 40);
    }
}